void main() {
  // printing Hello World
  print("Hello World!");
  // the output will be Hello World!

  // showing sum of int 1 and int 1.
  print("The sum of 1 and 1 is ${1 + 1}");
  // the output will be 2 because both are integers so they will be added.

  // concatenating string '3' and string '2'.
  print('3' + '2');
  // the output will be 32 because both are strings so they will be concatenated.

  // multiplying string '3' with int 2.
  print('3' * 2);
  // the output will be 33 because string '3' is repeated 2 times. if we multiply String 3 with int 5 then the output will be 33333.

  print(5 / 4 + 2 - 4 * 2);
  // the output will be -4.75 because according to operator precedence first multiplication and division will be done then addition and subtraction.

  print(75 / 5 + 2);
  // the output will be 17.0 because first division will be done then addition.

  print(75/(5+2));
  // the output will be 10.714285714285714 because first the operation inside the parenthesis will be done then division.
}
